package practice01;

// 한 개의 문자 값에 대한 자료형(type)은 char를 이용한다.

public class pr_char {
	public static void main(String[] args) {
		char a1 = 'a';
		char a2 = 97;
		char a3 = '\u0061';
		
		System.out.println(a1);  // 문자값 a
		System.out.println(a2);  // 아스키코드값 a
		System.out.println(a3);  // 유니코드값 a
		}
}
